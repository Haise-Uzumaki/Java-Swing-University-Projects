class Full {
    
   private int empID;
   public double salary;
   public double bonus;
   

   public void setEmpID(int empID){
    this.empID = empID;
   }

   public void setSalary(double s){
    this.salary = s;
   }

   public void setBonus(double bonus){
    this.bonus = bonus;
   }

   public int getEmpID(){
    return empID;
   }

   public double getSalary(){
    return salary;
   }

   public double getBonus(){
    return bonus;
   }



}
